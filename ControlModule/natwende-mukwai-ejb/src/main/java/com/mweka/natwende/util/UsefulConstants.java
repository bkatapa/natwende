package com.mweka.natwende.util;

public class UsefulConstants {
	public static String likePercentageBothEnds(String value) {
		return "%" + value + "%";
	}
	
	public static String likePercentageLeftEnd(String value) {
		return "%" + value;
	}
	
	public static String likePercentageRightEnd(String value) {
		return value + "%";
	}
}
